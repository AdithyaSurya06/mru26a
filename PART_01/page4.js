//-------operators---

//Arithmetic operators   [+,-,*,/,%]
console.log(10+2)     
//Comparision   [>,<,>=,<=,==,===]
console.log(10>5)      
console.log(10=="10")
console.log(10==="10")
//Assignment [+=,-=,*=]
let a=10  
a+=5
console.log(a)
//logical [&&,||,!]
console.log(10>3 && 10>15)
console.log(10>3 || 10>15)
console.log(!10>3)
//Unary operator [a++,a--]
let b=10
b++
console.log(b)
//Ternary operator
let c=10
c>11?console.log("hello"):console.log("world")
//[condition?{true}:{false}]
c>10&&console.log("hello")
